// Get the status of the entire board's switches
void readSwitches(switchArray* array) {

	int active = 0;

	// Loop through each column
	for (int column = 0; column < 8; ++column) {

		// Select the column and read it into the active vector
		setColumn(column);
		active = readColumn();

		for (int row = 0; row < 8; ++row) {

			// Loop through each row in the column vector. Set/Reset the corresponding bits in the switchArray structure
			if(active & (1 << row)) {
				array->set(column, row);
			} else {
				array->reset(column, row);
			}
		}

	}

}

